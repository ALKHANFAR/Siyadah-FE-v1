export function getSectorPrompt(sectorId: string): string {
  const sector = SECTOR_CONFIGS[sectorId];
  if (!sector) return "";

  return `## تخصيص القطاع: ${sector.nameAr}

استخدم المصطلحات التالية:
- العميل = "${sector.customerTerm}"
- الطلب = "${sector.orderTerm}"

المشاكل الشائعة في هالقطاع:
${sector.commonPainPoints.map((p) => `- ${p}`).join("\n")}

الأتمتات المقترحة:
${sector.suggestedAutomations.map((a) => `- ${a}`).join("\n")}

مؤشرات الأداء:
${sector.kpiExamples.map((k) => `- ${k}`).join("\n")}
`;
}
